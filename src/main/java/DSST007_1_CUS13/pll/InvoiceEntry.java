    public void getCusDetails() {
        
        Connection con = null;
        PreparedStatement prepstmt = null;
        ResultSet rs = null;

        try {
            con = DatabaseConnection.createConnection();
            String cusName = ((JTextField)cusName_jcb.getEditor().getEditorComponent()).getText();
            System.out.println("cusName: "+cusName);
            String sql = "SELECT name, address1, address2, state, pincode, phone1, GSTIN, PAN FROM customer_registration WHERE name=?";
            prepstmt = con.prepareStatement(sql);
            prepstmt.setString(1, cusName);
            rs = prepstmt.executeQuery();
            while(rs.next()) {
                cusAddressline1_jtf.setText(rs.getString("address1"));
                cusAddressline2_jtf.setText(rs.getString("address2"));
                cusState_jtf.setText(rs.getString("state"));
                cusPinCode_jtf.setText(rs.getString("pincode"));
                cusPhone1_jtf.setText(rs.getString("phone1"));
                cusGSTIN_jtf.setText(rs.getString("GSTIN"));
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "InvoiceEntry: "+e);
        }
      
    }
    
