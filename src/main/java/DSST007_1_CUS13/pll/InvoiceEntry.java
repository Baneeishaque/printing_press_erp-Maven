    private boolean validationBeforeNext()
    {
        
        boolean valid = true;
        
        if(((JTextField)invoiceNo_jcb.getEditor().getEditorComponent()).getText().equals("") || invoiceNo_jcb.getSelectedIndex() != -1)
        {
            
            JOptionPane.showMessageDialog(this,"Enter a valid Invoice Number.","Error",JOptionPane.ERROR_MESSAGE);
            valid = false;
            
        }
        else if(invDate_jdc.getDate() == null)
        {
            
            JOptionPane.showMessageDialog(this,"Enter a valid Invoice Date.","Error",JOptionPane.ERROR_MESSAGE);
            valid = false;
            
        }
        else if(cusName_jcb.getSelectedIndex() == -1)
        {
            
            
            JOptionPane.showMessageDialog(this,"Enter a valid Customer Name.","Error",JOptionPane.ERROR_MESSAGE);
            valid = false;
            
        }
        
        return valid;
        
    }
    
