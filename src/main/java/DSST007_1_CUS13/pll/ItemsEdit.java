    private void showItemDetails() {
        try {
            
            String itemName = ((JTextField)item_jcb.getEditor().getEditorComponent()).getText();
            InvoiceItems_BLL invoiceItems_BLL = new InvoiceItems_BLL();
            List<InvoiceItems_BLL> itemsDetList = invoiceItems_BLL.getItemDetails(itemName);
            
            for (int i = 0; i < itemsDetList.size(); i++) {
                
                String item = itemsDetList.get(i).getItemname();
                String hsnSac = itemsDetList.get(i).getHsn_sac();
                String tax = itemsDetList.get(i).getTax();
                String unit = itemsDetList.get(i).getUnit();
                
                ((JTextField)newItem_jcb.getEditor().getEditorComponent()).setText(item);
                hsnSac_jcb.setSelectedItem(hsnSac);
                ((JTextField)tax_jcb.getEditor().getEditorComponent()).setText(tax);
                unit_jcb.setSelectedItem(unit);
                
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "InvoiceAddItems: "+e);
        }
    }
    
