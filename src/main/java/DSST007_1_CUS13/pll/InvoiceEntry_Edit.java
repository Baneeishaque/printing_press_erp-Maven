                    cusAddressline2_jtf.setText(invDetList.get(i).getCus_addressline2());
                    cusState_jtf.setText(invDetList.get(i).getCus_state());
                    cusStateCode_jtf.setText(invDetList.get(i).getCus_state_code());
                    cusPAN_jtf.setText(invDetList.get(i).getCus_pan());
                    cusGSTIN_jtf.setText(invDetList.get(i).getCus_gstin());
                    bankName_jtf.setText(invDetList.get(i).getBank_name());
                    accNo_jtf.setText(invDetList.get(i).getAcc_no());
                    branch_jtf.setText(invDetList.get(i).getBranch());
                    iFSC_jtf.setText(invDetList.get(i).getIfsc());
                    dateOfSupply_jdc.setDate(invDetList.get(i).getDate_of_supply());
                    timeOfSuppy_jtp.setText(invDetList.get(i).getTime_of_supply());
                    itemVendorCode_jtf.setText(invDetList.get(i).getItem_vendor_code());
