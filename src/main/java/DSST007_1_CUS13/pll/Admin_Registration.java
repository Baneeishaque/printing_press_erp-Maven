    private void savedata()
    {
        
        Connection con = DatabaseConnection.createConnection();  
        boolean status = false;
        boolean error = false;
        
        try
        {
         
            if(txt_fname.getText().equals(""))
            {
                
                JOptionPane.showMessageDialog(this,"Please enter First Name.","Error",JOptionPane.ERROR_MESSAGE); 
                txt_fname.requestFocusInWindow();
                
            }
            else if(txt_uname.getText().equals(""))
            {
                
                JOptionPane.showMessageDialog(this,"Specify Username.","Error",JOptionPane.ERROR_MESSAGE); 
                txt_uname.requestFocusInWindow();

            }
            else if(new String(txt_password.getPassword()).equals(""))
            {
                
                JOptionPane.showMessageDialog(this,"Specify Password.","Error",JOptionPane.ERROR_MESSAGE); 
                txt_password.requestFocusInWindow();

            }
            else if(!(new String(txt_password.getPassword())).equals(new String(txt_repassword.getPassword())))
            {
                
                JOptionPane.showMessageDialog(this,"Passwords do not Match.","Error",JOptionPane.ERROR_MESSAGE); 
                txt_repassword.requestFocusInWindow();
                
            }
            else if(dc_dob.getDate() == null)
            {
                
                JOptionPane.showMessageDialog(this,"Specify DOB.","Error",JOptionPane.ERROR_MESSAGE); 
                dc_dob.requestFocusInWindow();
                
            }
            else if(cmb_secqn.getSelectedItem().toString().equals("-Select-"))
            {
                
                JOptionPane.showMessageDialog(this,"Specify a Security Question.","Error",JOptionPane.ERROR_MESSAGE); 
                cmb_secqn.requestFocusInWindow();
                
            }
            else if(txt_ans.getText().equals(""))
            {
                
                JOptionPane.showMessageDialog(this,"Specify an Answer to the Security Question.","Error",JOptionPane.ERROR_MESSAGE); 
                txt_ans.requestFocusInWindow();
                
            }
            else
            {
                String sql ="INSERT into tbl_adminreg(FNAME,LNAME,ADD1,ADD2,CITY,COUNTRY,STATE,PINCODE,PHONE,EMAIL,UNAME,PASSWORD,FIRMNAME,FADD1,FADD2,FCITY,FCOUNTRY,FSTATE,FPINCODE,FPHONE,FEMAIL,FWEBSITE,DOB,SECURITY, ANS)values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                PreparedStatement stmt = con.prepareStatement(sql);
                stmt.setString(1, txt_fname.getText());
                stmt.setString(2, txt_lname.getText());
                stmt.setString(3, txt_add1.getText());
                stmt.setString(4, txt_add2.getText());
                stmt.setString(5, txt_city.getText());
                stmt.setString(6, cmb_country.getSelectedItem().toString());
                stmt.setString(7, cmb_state.getSelectedItem().toString());
                stmt.setString(8,txt_pincode.getText());
                stmt.setString(9,(txt_phone.getText()));
                stmt.setString(10,txt_email.getText());
                stmt.setString(11, txt_uname.getText());
                stmt.setString(12,new String(txt_repassword.getPassword()));
                //stmt.setString(13,txt_repassword.getText());
                stmt.setString(13, "");
                stmt.setString(14, "");
                stmt.setString(15, "");
                stmt.setString(16, "");
                stmt.setString(17, "");
                stmt.setString(18, "");
                stmt.setString(19,"");
                stmt.setString(20, "");
                stmt.setString(21,"");
                stmt.setString(22,"");
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
                stmt.setString(23, sdf.format(dc_dob.getDate()));
                stmt.setString(24, cmb_secqn.getSelectedItem().toString());
                stmt.setString(25,txt_ans.getText());

                stmt.executeUpdate();

                status = true;
                
            }
         
        }
        catch(Exception e)
        {
        
            error = true;
            JOptionPane.showMessageDialog(null,e);
            
    
        }       
        finally
        {
    
            try
            {

                if(con!=null)
                con.close();

            }
            catch(SQLException se)
            {

                se.printStackTrace();

            }
        } 
        
        
        if(status == true && error == false)
        {
            
            JOptionPane.showMessageDialog(this,"Operation Successful.","Success",JOptionPane.INFORMATION_MESSAGE);

            this.setVisible(false);
            Login loginForm = new Login();
            loginForm.setVisible(true);   
            
            
        }
 
        
    }
    
