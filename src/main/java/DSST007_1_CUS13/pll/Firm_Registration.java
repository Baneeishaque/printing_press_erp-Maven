    private void getFirmDetails()
    {

        Connection con = DatabaseConnection.createConnection();

        try
        {

            Statement stmt = con.createStatement();
            Statement stmt2 = con.createStatement();
            ResultSet rs = stmt.executeQuery(" SELECT * FROM firm_registration ");
            ResultSet rs2 = stmt2.executeQuery(" SELECT * FROM firm_registration ");
            
            if(rs.first() == false)
            {
                
                save_jb.setEnabled(true);
                edit_jb.setEnabled(false);
                update_jb.setEnabled(false);
                delete_jb.setEnabled(false);
                
                enableFields();
                
                Date date = new Date();
                regdate_jdc.setDate(date);
                
            }
            else
            {
               
                save_jb.setEnabled(false);
                edit_jb.setEnabled(true);
                update_jb.setEnabled(false);
                delete_jb.setEnabled(false);
 
                disableFields();

                while(rs2.next())
                {

                    String firmname = rs.getString("name");
                    firmname_jtf.setText(firmname);
                    System.out.println(firmname);

                    String address1 = rs.getString("address1");
                    address1_jtf.setText(address1);

                    String address2 = rs.getString("address2");
                    address2_jtf.setText(address2);

                    String city = rs.getString("city");
                    city_jtf.setText(city);

                    String district = rs.getString("district");
                    district_jtf.setText(district);

                    String state = rs.getString("state");
                    state_jtf.setText(state);

                    String country = rs.getString("country");
                    country_jcb.setSelectedItem(country);

                    String pincode = rs.getString("pincode");
                    pincode_jtf.setText(pincode);

                    String phone1 = rs.getString("phone1");
                    phone1_jtf.setText(phone1);

                    String phone2 = rs.getString("phone2");
                    phone2_jtf.setText(phone2);

                    String fax = rs.getString("fax");
                    fax_jtf.setText(fax);

                    String email = rs.getString("email");
                    email_jtf.setText(email);

                    String website = rs.getString("website");
                    website_jtf.setText(website);

                    Date regdate = rs.getDate("regdate");
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy",Locale.getDefault());
                    regdate_jdc.setDate(sdf.parse(sdf.format(regdate)));

                    String bankname = rs.getString("bankname");
                    bankname_jtf.setText(bankname);

                    String accname = rs.getString("accname");
                    accname_jtf.setText(accname);

                    String branch = rs.getString("branch");
                    bankloc_jtf.setText(branch);

                    String ifsc = rs.getString("ifsc");
                    ifsc_jtf.setText(ifsc);

                    String accno = rs.getString("accno");
                    accno_jtf.setText(accno);

                    String iban = rs.getString("iban");
                    iban_jtf.setText(iban);

                    tableid = rs.getString("id");

                } 
                
                save_jb.setEnabled(false);
                edit_jb.setEnabled(true);
                
            }

             
        }
        catch(Exception e)
        {
             
            JOptionPane.showMessageDialog(null, e);
             
        }
        finally
                {
                    
                    if(con!=null)
                    {
                        try {
                            con.close();
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(null, ex);
                        }
                        
                    }
                    
                }
        //-----------------

        
        
    }
    
